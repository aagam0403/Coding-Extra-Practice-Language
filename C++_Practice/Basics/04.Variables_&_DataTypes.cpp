#include <iostream>
using namespace std;
int main() 
{
    int age = 20;         // Integer
    float weight = 65.5;  // Floating point
    char grade = 'A';     // Character
    bool isPass = true;   // Boolean
    string name = "Aagam";// String (from C++ Standard Library)

    cout << "Age: " << age << ", Weight: " << weight << endl;
    cout << "Grade: " << grade << ", Passed: " << isPass << endl;
    cout << "Name: " << name << endl;
}